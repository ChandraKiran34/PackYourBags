import mongoose from 'mongoose';

const guideSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  phoneNumber: {
    type: String,
    required: true
  },
  location: {
    type: String,
    required: true
  }
});

const Guide = mongoose.model('Guide', guideSchema);

export default Guide;
